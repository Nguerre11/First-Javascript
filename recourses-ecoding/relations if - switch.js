let day = "Monday";

switch (day) {
  case "Monday":
    console.log("It's the start of a new week!");
    break;
  case "Tuesday":
    console.log("Only a few more days until the weekend.");
    break;
  case "Wednesday":
    console.log("Halfway through the week.");
    break;
  case "Thursday":
    console.log("One more day until the weekend.");
    break;
  case "Friday":
    console.log("It's finally Friday!");
    break;
  case "Saturday":
    console.log("Time to relax and enjoy the weekend.");
    break;
  case "Sunday":
    console.log("Almost time to go back to work.");
    break;
  default:
    console.log("Invalid day of the week.");
}
